import type { Metadata } from "next";
import { LegalPageShell, DynamicLegalSections } from "../LegalPageShell";
import { getDynamicPageBlock } from "../legal-data";

export const revalidate = 300;

export const metadata: Metadata = {
  title: "Политика конфиденциальности | Робокс Липецк",
  description: "Политика обработки персональных данных на сайте школы робототехники Робокс.",
};

export default async function PrivacyPage() {
  const pageBlock = await getDynamicPageBlock(
    "legal.page.privacy",
    "Политика обработки персональных данных",
    "Документ описывает, какие данные собирает сайт Робокс, для каких целей они используются и как пользователь может обратиться по вопросам обработки данных.",
    `### 1. Общие положения
Настоящая политика действует для сайта и сервисов школы робототехники Робокс. Отправляя форму заявки, пользователь подтверждает согласие на обработку персональных данных в объёме, необходимом для связи, записи на занятия, сопровождения обучения и оплаты услуг.
### 2. Какие данные собираются
- ФИО родителя или законного представителя.
- Телефон и email для связи.
- Имя ребёнка, возраст, выбранная группа и направление обучения.
- Данные заявок, комментарии к заявкам и история коммуникаций.
- Данные об оплатах, счетах и статусах платежей без хранения полных данных банковской карты на сайте.
### 3. Цели обработки
Данные используются для обработки заявок, подбора группы, связи с родителем, ведения CRM, отображения информации в личном кабинете, выставления счетов, фиксации оплат, информирования о расписании и исполнения обязательств по договору оказания услуг.
### 4. Хранение и защита
Данные хранятся в CRM и связанных сервисах столько, сколько необходимо для оказания услуг, выполнения требований закона, бухгалтерского учёта и защиты прав сторон. Доступ к данным предоставляется только сотрудникам, которым он нужен для работы.
### 5. Права пользователя
Пользователь может запросить информацию о своих данных, уточнить их, отозвать согласие на обработку или запросить удаление данных, если их хранение больше не требуется по закону или договору.`
  );

  return (
    <LegalPageShell
      title={pageBlock.title}
      lead={pageBlock.subtitle}
    >
      <DynamicLegalSections bodyText={pageBlock.body} />
    </LegalPageShell>
  );
}
